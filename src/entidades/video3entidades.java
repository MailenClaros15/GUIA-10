package entidades;

/**
 *
 * @author Edgardo
 */
public class video3entidades {
    
    private String nombre;
    private String apodo;
    private String tipo;

    public video3entidades() {
    }

    public video3entidades(String nombre, String apodo, String tipo) {
        this.nombre = nombre;
        this.apodo = apodo;
        this.tipo = tipo;
    }


 

    public String getTipo() {
        return tipo;
    }

    public String getNombre() {
        return nombre;
    }

    public String getApodo() {
        return apodo;
    }

 

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public void setApodo(String apodo) {
        this.apodo = apodo;
    }


    }





    
    

